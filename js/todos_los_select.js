/*-Funcion que recorre arreglo y modifica option y value en el select-*/
function addOptions(elemento_en_arreglo, valor_arreglo) {
 var select = document.getElementsByName(elemento_en_arreglo)[0];
 for (value in valor_arreglo) {
  var option = document.createElement("option");
  option.text = valor_arreglo[value];
  option.setAttribute("value", valor_arreglo[value]);
  select.add(option);
 }
}

